def tuples_2_numpy(data):
    '''
    [3,1] ===> [1,1,1]
    :param data:
    :return:
    '''
    classVec = []
    for tuples in data:
        number = tuples[0]
        element = tuples[1]
        for n in range(0, number):
            classVec.append(element)
    return classVec
